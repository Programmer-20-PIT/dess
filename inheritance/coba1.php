class otomotif {
  public $merk;
  public $warna;

  // melakukan construct
  public function __construct($merk,$warna) {
    $this->merk = $merk;
    $this->warna = $warna;
  }

  // melakukan method
  public function klakson () {
    return "tin tin";
  }
  protected function  infootomotif () {
    return "tin tin" . $this->merk. ", warna:" . $this->warna;
  }
}
